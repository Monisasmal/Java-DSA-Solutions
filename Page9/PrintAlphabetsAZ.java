// Write a program to print Alphabets from (A to Z).

package Page9;
public class PrintAlphabetsAZ {
  public static void main(String args[]){
    for(char ch = 'A'; ch <= 'Z'; ch++){
      System.out.println(ch);
    }
  }
}

